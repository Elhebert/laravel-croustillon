<h3>
    Qu’est-ce qu’un cookie&nbsp;?
</h3>

<p>
    Un «&nbsp;cookie&nbsp;» est un petit fichier texte contenant des informations sauvegardées par un site web sur un ordinateur ou par une application mobile sur le smartphone d’un utilisateur. Les cookies offrent la possibilité de vous identifier et de mémoriser différentes informations aux fins de vous faciliter la navigation sur un site/une application mobile, d’assurer le bon fonctionnement de ceux-ci ou de les rendre plus efficaces, par exemple en mémorisant vos préférences linguistiques.
</p>
