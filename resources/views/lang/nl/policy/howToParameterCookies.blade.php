<h3>
    Hoe moet je de cookies instellen&nbsp;?
</h3>
<p>
    U kunt op elk moment eenvoudig de cookies van onze site verwijderen, uitschakelen of accepteren door uw browserinstellingen te configureren.
</p>
<p>
    Elke browser (Internet Explorer, Safari, Firefox, Google Chrome, enz.) heeft zijn eigen cookie-configuratiemodus. Voor de procedure die u voor uw browser moet volgen, gaat u naar&nbsp;:&nbsp;<a href="http://www.allaboutcookies.org/manage-cookies/">http://www.allaboutcookies.org/manage-cookies/</a>.
</p>
<p>
    Als u verschillende computers, smartphones en / of tablets gebruikt, vergeet dan niet elk apparaat zo te configureren dat elk apparaat qua cookies overeenkomt met uw voorkeuren.
</p>
