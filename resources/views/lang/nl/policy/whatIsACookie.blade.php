<h3>
    Wat is een cookie&nbsp;?
</h3>
<p>
    Een "cookie" is een klein tekstbestand met informatie die wordt opgeslagen door een website op een computer of door een mobiele applicatie op de smartphone van een gebruiker. Cookies bieden de mogelijkheid om u te identificeren en verschillende informatie te onthouden om u te helpen bij het navigeren op een site / een mobiele applicatie, om deze goed te laten functioneren of om ze effectiever te maken, bijvoorbeeld door uw taalvoorkeuren.
</p>
