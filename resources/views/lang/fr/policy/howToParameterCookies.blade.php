<h3>
    Comment paramétrer les cookies&nbsp;?
</h3>

<p>
    Vous pouvez facilement supprimer, désactiver ou accepter à tout moment les cookies de notre Site en configurant les paramètres de votre navigateur.
</p>

<p>
    Chaque navigateur (Internet Explorer, Safari, Firefox, Google Chrome, etc.) a son propre mode de configuration de cookies. Pour connaître la procédure à suivre concernant votre navigateur, rendez-vous sur le site&nbsp;:&nbsp;
    <a href="http://www.allaboutcookies.org/fr/gerer-les-cookies/">http://www.allaboutcookies.org/fr/gerer-les-cookies/</a>.
</p>

<p>
    Si vous utilisez différents ordinateurs, smartphones et/ou tablettes, n’oubliez pas de configurer chaque équipement de manière à ce que chacun d’eux corresponde à vos préférences en termes de cookies.
</p>
